#include "libft.h"
#include <stdlib.h>
#include <string.h>

char	*ft_strnew(size_t size)
{
	char *str;

	if (!(str = (char*)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	else
	{
		ft_bzero((void*)str, size + 1);
		return (str);
	}
	return (0);
}
